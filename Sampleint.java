public class Sampleint{
       public static void main(String[]args){
       int number= 7;


           switch(number){
     
           case 0:
                  System.out.println("ZERO IS VIOLET");
                     break;

           case 1:
                  System.out.println("ONE IS INDIGO");
                   break;
    
           case 2:
                  System.out.println("TWO IS BLUE");
                    break;
  
           case 3:
                 System.out.println("THREE IS GREEN");
                  break;

           case 4:
                System.out.println("FOUR IS YELLOW");
                  break;

           case 5:
                   System.out.println("FIVE IS ORANGE");
                     break;

           case 6:
                   System.out.println("SIX IS RED");
                     break;

           default:
                   System.out.println("ITS AN OUTLIER");

                     break;

           




           }
           }
           }